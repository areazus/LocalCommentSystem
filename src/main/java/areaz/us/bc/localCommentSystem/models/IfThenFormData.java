package areaz.us.bc.localCommentSystem.models;

public class IfThenFormData {
	private String field, value, condition, equalsString, color;

	public String getField() {
		return field;
	}

	public void setField(String field) {
		this.field = field;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public String getCondition() {
		return condition;
	}

	public void setCondition(String condition) {
		this.condition = condition;
	}

	public String getEqualsString() {
		return equalsString;
	}

	public void setEqualsString(String equalsString) {
		this.equalsString = equalsString;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}	
	
}
